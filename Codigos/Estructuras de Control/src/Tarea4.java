import javax.swing.*;
public class Tarea4 {
    /*
    Escribe un programa en Java que use un bucle while para sumar los números del 1 al 100.
     */
    public static void main(String[]args){
        JOptionPane.showMessageDialog(null,"Buenos Días Bienvenido al Temporizador");
        int i=1;

        while(i<=100){
            System.out.println(i);
            i++;
        }
    }
}
